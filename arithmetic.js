var friends = 10;
console.log("Intially:", friends);

// friends = friends + 1;
friends += 1;
console.log("After adding 1:", friends);

// friends = friends - 1;
friends -= 1;
console.log("After substracting 1:", friends);

// friends = friends * 2;
friends *= 2;
console.log("After multiplying with 2:", friends);

// friends = friends / 4;
friends /= 4;
console.log("After dividing by 4:", friends);

// friends = friends ** 2;
friends **= 2;
console.log("After raising by power 2:", friends);

// friends = friends % 3;
friends %= 3;
console.log("Modulus of 3:", friends);

friends = 10 + 2 / 2;
console.log(friends);

